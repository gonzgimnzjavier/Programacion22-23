package resueltos;
//Mostrar por pantalla todos los numeros entre dos

public class resuelto6 {
	
	public static void main(String[] args) {
		for(int i=200; i<300+1; i++) {
			System.out.println(i);
		}
		int j=200;
		do {
			System.out.println(j);
			j++;
		}while(j<300+1);
		}
	}


